function buildRows(plan, seen) {
  const rows = [];
  let sequence = 1;

  for (const [root, meaningCore, advice] of plan.roots) {
    for (const context of contexts) {
      if (rows.length >= TARGET_PER_CATEGORY) return rows;
      const keyword = `ฝันเห็น${root}${context}`;
      if (seen.has(normalizeKeyword(keyword))) continue;
      seen.add(normalizeKeyword(keyword));
      const hash = hashText(`${plan.category}:${keyword}`);
      rows.push({
        id: `${plan.prefix}-${String(sequence).padStart(4, "0")}`,
        keyword,
        letter: firstThaiLetter(root),
        category: plan.category,
        meaning: buildMeaning(keyword, meaningCore, hash),
        numbers: buildNumbers(hash),
        time: timeHints[hash % timeHints.length],
        advice,
        updated_at: new Date().toISOString(),
      });
      sequence += 1;
    }
  }

  if (rows.length < TARGET_PER_CATEGORY) {
    throw new Error(`Generated only ${rows.length} rows for ${plan.category}`);
  }

  return rows;
}

function buildMeaning(keyword, core, hash) {
  const opener = openers[hash % openers.length];
  const transition = transitions[Math.floor(hash / 7) % transitions.length];
  const closer = closers[Math.floor(hash / 17) % closers.length];
  return `${opener} โดยเฉพาะเมื่อ${keyword.replace(/^ฝันเห็น/, "เห็น")} ความหมายหลักคือ ${core} ${transition} ${closer}`;
}

function buildNumbers(hash) {
  const twoA = String(hash % 100).padStart(2, "0");
  const twoB = String(Math.floor(hash / 13) % 100).padStart(2, "0");
  const three = String(Math.floor(hash / 97) % 1000).padStart(3, "0");
  return `${twoA}, ${twoB}, ${three}`;
}

function firstThaiLetter(text) {
  return Array.from(text).find((char) => /[ก-ฮ]/.test(char)) || "";
}

function normalizeKeyword(text) {
  return text.replace(/\s+/g, " ").trim().toLowerCase();
}

function hashText(text) {
  let hash = 2166136261;
  for (const char of text) {
    hash ^= char.codePointAt(0);
    hash = Math.imul(hash, 16777619) >>> 0;
  }
  return hash;
}

async function loadExistingKeywords() {
  const keywords = new Set();
  let offset = 0;

  while (true) {
    const rows = await request(`dreams?select=keyword&limit=1000&offset=${offset}&order=keyword.asc`);
    if (!Array.isArray(rows) || rows.length === 0) break;
    for (const row of rows) {
      if (row?.keyword) keywords.add(normalizeKeyword(row.keyword));
    }
    if (rows.length < 1000) break;
    offset += rows.length;
  }

  console.log(`Loaded ${keywords.size} existing dream keywords`);
  return keywords;
}

async function request(path, options = {}) {
  const response = await fetch(`${supabaseUrl}/rest/v1/${path}`, {
    method: options.method || "GET",
    headers: {
      apikey: serviceKey,
      Authorization: `Bearer ${serviceKey}`,
      ...(options.body ? { "Content-Type": "application/json" } : {}),
      ...(options.headers || {}),
    },
    body: options.body,
  });

  if (!response.ok) {
    const detail = await response.text();
    throw new Error(`Supabase request failed ${response.status}: ${detail}`);
  }

  if (response.status === 204) return null;
  const text = await response.text();
  return text ? JSON.parse(text) : null;
}

async function loadLocalEnv() {
  try {
    const text = await readFile(new URL("../.env.local", import.meta.url), "utf8");
    for (const line of text.split(/\r?\n/)) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) continue;
      const [key, ...rest] = trimmed.split("=");
      if (!process.env[key]) process.env[key] = rest.join("=").replace(/^"|"$/g, "");
    }
  } catch {
    // Vercel/CI can provide env directly.
  }
}
