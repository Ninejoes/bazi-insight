function letterOf(keyword) {
  return [...keyword.trim()][0] || "";
}

function stableNumbers(keyword, index) {
  let hash = 0;
  for (const char of keyword) hash = (hash * 31 + char.charCodeAt(0)) % 10000;
  const a = (hash + index * 7) % 100;
  const b = (hash * 3 + index * 11) % 100;
  const c = (hash * 7 + index * 13) % 100;
  return [a, b, c]
    .map((n) => String(n).padStart(2, "0"))
    .filter((value, i, arr) => arr.indexOf(value) === i)
    .join(", ");
}

function buildMeaning(keyword, category, index) {
  const profile = categoryProfiles[category];
  const alt =
    index % 3 === 0
      ? `อีกความเป็นไปได้คือมีข่าวหรือคำพูดจากคนใกล้ตัวที่ทำให้ต้องทบทวนท่าทีของตัวเอง`
      : index % 3 === 1
        ? `อีกความเป็นไปได้คือเรื่องนี้สะท้อนความกังวลที่เก็บไว้ และกำลังรอเวลาจัดการให้ชัด`
        : `อีกความเป็นไปได้คือจะมีจังหวะเปลี่ยนแผนเล็ก ๆ แต่กลับช่วยให้เห็นทางออกง่ายขึ้น`;
  return `ฝันเห็น${keyword} ตามตำราฝันไทยมักโยงกับ${profile.theme}. โดยภาพรวมถือเป็นสัญญาณว่า${profile.positive}. ${alt}. จุดที่ควรระวังคือ${profile.caution}.`;
}

function buildAdvice(category, index) {
  const advice = advicePool[index % advicePool.length];
  const profile = categoryProfiles[category];
  return `${advice} ถ้าจะใช้เลขหรือคำทำนาย ควรใช้เป็นแนวทางเสริมสติ ไม่แทนการตัดสินใจหลัก; ${profile.caution}.`;
}

const entries = categories
  .flatMap((category) => category.keywords.map((keyword) => ({ keyword, category: category.name })))
  .slice(0, 200)
  .map((entry, index) => ({
    id: `dream-dict-${String(index + 1).padStart(3, "0")}`,
    keyword: entry.keyword,
    letter: letterOf(entry.keyword),
    category: entry.category,
    meaning: buildMeaning(entry.keyword, entry.category, index),
    numbers: stableNumbers(entry.keyword, index),
    time: timePool[index % timePool.length],
    advice: buildAdvice(entry.category, index),
    updated_at: new Date().toISOString(),
  }));

const legacyIds = [
  "dream-snake",
  "dream-clear-water",
  "dream-monk",
  "dream-broken-tooth",
  "dream-gold",
];

const deleteLegacy = await fetch(
  `${supabaseUrl}/rest/v1/dreams?id=in.(${legacyIds.map(encodeURIComponent).join(",")})`,
  {
    method: "DELETE",
    headers: {
      apikey: serviceKey,
      Authorization: `Bearer ${serviceKey}`,
    },
  },
);

if (!deleteLegacy.ok) {
  const detail = await deleteLegacy.text().catch(() => "");
  console.error(`Supabase legacy dream cleanup failed ${deleteLegacy.status}: ${detail}`);
  process.exit(1);
}

const response = await fetch(`${supabaseUrl}/rest/v1/dreams?on_conflict=id`, {
  method: "POST",
  headers: {
    "Content-Type": "application/json",
    apikey: serviceKey,
    Authorization: `Bearer ${serviceKey}`,
    Prefer: "resolution=merge-duplicates,return=minimal",
  },
  body: JSON.stringify(entries),
});

if (!response.ok) {
  const detail = await response.text().catch(() => "");
  console.error(`Supabase dreams seed failed ${response.status}: ${detail}`);
  process.exit(1);
}

console.log(`Seeded ${entries.length} dream dictionary records.`);
