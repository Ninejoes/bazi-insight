function makeArticle(category, topic, index) {
  const [baseSlug, title, excerpt] = topic;
  const slug = slugify(baseSlug, index);
  const cover = pick(imagePools[category], index);
  const readMin = 4 + (index % 4);
  const content =
    category === "ปาจื้อ"
      ? baziContent(title, excerpt, index)
      : category === "ไพ่ยิปซี"
        ? tarotContent(title, excerpt, index)
        : dreamContent(title, excerpt, index);

  return {
    slug,
    title,
    excerpt,
    category,
    author: authorByCategory[category],
    date: dateFor(index),
    read_min: readMin,
    cover,
    cover_alt: `${title} - ภาพประกอบฟรีจาก Unsplash`,
    seo_title: `${title} | Likhitfa`,
    seo_description: excerpt.slice(0, 155),
    keywords: keywordSet(category, title).slice(0, 8),
    canonical_url: `${siteUrl}/articles/${slug}`,
    content,
  };
}

function expandTopics(topics, target) {
  const variants = [
    "ฉบับเริ่มต้น",
    "สำหรับมือใหม่",
    "แบบใช้งานจริง",
    "พร้อมตัวอย่าง",
    "ข้อควรระวัง",
    "อ่านให้ลึกขึ้น",
    "ใช้วางแผนชีวิต",
    "มุมมองร่วมสมัย",
  ];
  const expanded = [];
  for (let i = 0; expanded.length < target; i += 1) {
    const topic = topics[i % topics.length];
    const round = Math.floor(i / topics.length);
    if (round === 0) {
      expanded.push(topic);
    } else {
      expanded.push([
        `${topic[0]}-${round + 1}`,
        `${topic[1]} ${pick(variants, round + i)}`,
        `${topic[2]} พร้อมแนวทางอ่านเพิ่มเติมและข้อสังเกตที่นำไปใช้ได้จริง`,
      ]);
    }
  }
  return expanded;
}

function buildArticles() {
  const plan = [
    ["ปาจื้อ", expandTopics(baziTopics, 70)],
    ["ไพ่ยิปซี", expandTopics(tarotTopics, 70)],
    ["ทำนายฝัน", expandTopics(dreamTopics, 60)],
  ];
  const articles = [];
  let index = 0;
  for (const [category, topics] of plan) {
    for (const topic of topics) {
      articles.push(makeArticle(category, topic, index));
      index += 1;
    }
  }
  return articles;
}

async function upsertArticles(articles) {
  const url = (process.env.SUPABASE_URL || "").replace(/\/$/, "");
  const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY || "";
  if (!url || !serviceKey) {
    throw new Error("Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY");
  }

  const chunkSize = 50;
  let inserted = 0;
  for (let i = 0; i < articles.length; i += chunkSize) {
    const chunk = articles.slice(i, i + chunkSize);
    const response = await fetch(`${url}/rest/v1/articles?on_conflict=slug`, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        apikey: serviceKey,
        Authorization: `Bearer ${serviceKey}`,
        Prefer: "resolution=merge-duplicates,return=minimal",
      },
      body: JSON.stringify(chunk),
    });
    if (!response.ok) {
      const detail = await response.text().catch(() => "");
      throw new Error(`Supabase insert failed ${response.status}: ${detail}`);
    }
    inserted += chunk.length;
    console.log(`Upserted ${inserted}/${articles.length}`);
  }
}

