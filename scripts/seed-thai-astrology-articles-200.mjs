function makeArticle([category, title], index) {
  const slug = slugifyThaiTitle(category, title, index);
  const description = descriptionFor(category, title);
  const cover = pick(imagesByCategory[category] || imagesByCategory["ความเชื่อไทย"], index);
  const keywords = Array.from(
    new Set([primaryKeyword(title), ...secondaryKeywords(category, title)]),
  ).slice(0, 10);
  return {
    slug,
    title,
    excerpt: description,
    category,
    author: categoryAuthors[category] || "ทีม Likhitfa",
    date: dateFor(index),
    read_min: 9 + (index % 5),
    cover,
    cover_alt: `${title} ภาพประกอบบทความ${category}`,
    seo_title: metaTitle(title),
    seo_description: description.slice(0, 160),
    keywords,
    canonical_url: `${siteUrl}/articles/${slug}`,
    content: buildContent(category, title),
    updated_at: new Date().toISOString(),
  };
}

async function upsertArticles(articles) {
  const url = (process.env.SUPABASE_URL || "").replace(/\/$/, "");
  const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_SERVICE_KEY || "";
  if (!url || !serviceKey) throw new Error("Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY");

  for (let index = 0; index < articles.length; index += 25) {
    const chunk = articles.slice(index, index + 25);
    const response = await fetch(`${url}/rest/v1/articles?on_conflict=slug`, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        apikey: serviceKey,
        Authorization: `Bearer ${serviceKey}`,
        Prefer: "resolution=merge-duplicates,return=minimal",
      },
      body: JSON.stringify(chunk),
    });
    if (!response.ok) {
      const detail = await response.text().catch(() => "");
      throw new Error(`Supabase upsert failed ${response.status}: ${detail}`);
    }
    console.log(`Upserted ${Math.min(index + chunk.length, articles.length)}/${articles.length}`);
  }
}

async function verifyCount() {
  const url = (process.env.SUPABASE_URL || "").replace(/\/$/, "");
  const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_SERVICE_KEY || "";
  const response = await fetch(`${url}/rest/v1/articles?select=category`, {
    headers: {
      apikey: serviceKey,
      Authorization: `Bearer ${serviceKey}`,
      Prefer: "count=exact",
    },
  });
  const rows = await response.json();
  const counts = {};
  for (const row of rows) counts[row.category] = (counts[row.category] || 0) + 1;
  return counts;
}

