function loadEnv() {
  try {
    const env = readFileSync(".env.local", "utf8");
    for (const line of env.split(/\r?\n/)) {
      const match = line.match(/^([A-Z0-9_]+)=(.*)$/);
      if (!match || process.env[match[1]]) continue;
      process.env[match[1]] = match[2].replace(/^"|"$/g, "");
    }
  } catch {
    // Environment can also be provided by shell/Vercel.
  }
}

function slugifyThaiTitle(category, title, index) {
  const suffix = String(index + 1).padStart(3, "0");
  for (const [pattern, slug] of titleSlugWords) {
    const match = title.match(pattern);
    if (match) return `${slug.replace("$1", match[1])}-${suffix}`;
  }
  const prefix = roman.get(category) || "article";
  const normalized = title
    .replace(/^พระประจำวัน/, "buddha-")
    .replace(/^สีมงคลประจำวัน/, "lucky-color-")
    .replace(/^ราศีไหนเข้ากับ/, "zodiac-compatible-with-")
    .replace(/^จุดเด่นของ/, "strengths-of-")
    .replace(/^ความรักคน/, "love-for-")
    .replace(/^ฤกษ์/, "auspicious-date-")
    .replace(/^ฮวงจุ้ย/, "fengshui-")
    .replace(/^ต้นไม้มงคล/, "lucky-plant-")
    .replace(/^หินมงคล/, "lucky-stone-")
    .replace(/^ความเชื่อเรื่อง/, "belief-")
    .replace(/^ฝันเห็น/, "dream-of-")
    .replace(/^ไฝที่/, "mole-on-")
    .replace(/^เส้น/, "palm-line-")
    .replace(/^ทายนิสัยจาก/, "personality-from-")
    .replace(/^ปี/, "year-of-");
  const ascii = normalized
    .normalize("NFKD")
    .replace(/[^\w\s-]/g, "")
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-");
  return `${prefix}-${ascii || "guide"}-${suffix}`;
}

