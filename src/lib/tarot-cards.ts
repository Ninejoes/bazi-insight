export function getCategory(slug: string) {
  return tarotCategories.find((c) => c.slug === slug);
}

export function pickRandomCards(n: number): { card: TarotCard; reversed: boolean }[] {
  const pool = [...tarotCards];
  const result: { card: TarotCard; reversed: boolean }[] = [];
  for (let i = 0; i < n && pool.length > 0; i++) {
    const idx = Math.floor(Math.random() * pool.length);
    const [c] = pool.splice(idx, 1);
    result.push({ card: c, reversed: Math.random() < 0.3 });
  }
  return result;
}
